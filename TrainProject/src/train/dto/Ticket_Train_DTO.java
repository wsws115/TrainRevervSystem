package train.dto;

public class Ticket_Train_DTO {
	String name;
	String num;
	String st_sub;
	String en_sub;
	String date;
	String st_time;
	String en_time;
	String train_ho;
	String seat;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getSt_sub() {
		return st_sub;
	}
	public void setSt_sub(String st_sub) {
		this.st_sub = st_sub;
	}
	public String getEn_sub() {
		return en_sub;
	}
	public void setEn_sub(String en_sub) {
		this.en_sub = en_sub;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getSt_time() {
		return st_time;
	}
	public void setSt_time(String st_time) {
		this.st_time = st_time;
	}
	public String getEn_time() {
		return en_time;
	}
	public void setEn_time(String en_time) {
		this.en_time = en_time;
	}
	public String getTrain_ho() {
		return train_ho;
	}
	public void setTrain_ho(String train_ho) {
		this.train_ho = train_ho;
	}
	public String getSeat() {
		return seat;
	}
	public void setSeat(String seat) {
		this.seat = seat;
	}
	
}
